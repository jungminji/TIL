function addition(n1, n2) {
  return n1 + n2;
}

function subtraction(n1, n2) {
  return n1 - n2;
}


function division(n1, n2) {
  return n1 / n2;
}

function multiplication(n1, n2) {
  return n1 * n2;
}


function remainder(n1, n2) {
  return n1 % n2;
}

var pi = 3.1415;

export{
  addition,
  subtraction,
  division,
  multiplication,
  remainder,
  pi
}
